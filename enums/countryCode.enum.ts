// Type of all codes: "IND" | "BRA" | "USA" | ...
export type CountryCode = typeof CountryCode[keyof typeof CountryCode];

// Lookup map for name â†’ code
export const CountryNameToCode: Record<string, CountryCode> = Object.fromEntries(
  Object.entries(CountryCode).map(([name, code]) => [name.toLowerCase(), code])
) as Record<string, CountryCode>;

// Runtime validator
export const isValidCountry = (country: string): country is CountryCode => {
  return Object.values(CountryCode).includes(country as CountryCode);
};
